type emojiEntry struct{ seq, code string }

var (
	// emojiReplacer replaces emoji sequences with :shortcode: form.
	emojiReplacer *strings.Replacer
	// sortedEntries is sorted by sequence byte-length descending for correct prefix matching.
	sortedEntries []emojiEntry
)

func init() {
	seen := map[string]bool{}
	for _, e := range rawEmoji {
		if !seen[e[0]] {
			seen[e[0]] = true
			sortedEntries = append(sortedEntries, emojiEntry{e[0], e[1]})
		}
	}
	sort.Slice(sortedEntries, func(i, j int) bool {
		return len(sortedEntries[i].seq) > len(sortedEntries[j].seq)
	})

	pairs := make([]string, 0, len(sortedEntries)*2)
	for _, e := range sortedEntries {
		pairs = append(pairs, e.seq, e.code)
	}
	emojiReplacer = strings.NewReplacer(pairs...)
}

// ReplaceEmoji replaces emoji characters in s with their :shortcode: equivalents.
func ReplaceEmoji(s string) string {
	return emojiReplacer.Replace(s)
}

// FindEmojiFindings returns a Finding for each emoji occurrence in content.
// Uses sortedEntries (longest sequences first) for correct prefix matching.
func FindEmojiFindings(relPath, content string) []Finding {
	var findings []Finding
	for lineNum, line := range strings.Split(content, "\n") {
		for bytePos := 0; bytePos < len(line); {
			matched := false
			for _, e := range sortedEntries {
				if strings.HasPrefix(line[bytePos:], e.seq) {
					findings = append(findings, Finding{
						File:    relPath,
						Line:    lineNum + 1,
						Col:     bytePos + 1,
						Message: "emoji - use " + e.code,
					})
					bytePos += len(e.seq)
					matched = true
					break
				}
			}
			if !matched {
				_, size := utf8.DecodeRuneInString(line[bytePos:])
				bytePos += size
			}
		}
	}
	return findings
}
